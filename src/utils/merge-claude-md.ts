const START_MARKER = "<!-- Generated by claude-code-loops -->";
const END_MARKER = "<!-- End claude-code-loops section -->";

/**
 * Merge incoming CLAUDE.md content into an existing file.
 * Uses HTML comment markers so re-running is safe (replaces the generated section).
 * If no existing content, returns the incoming content with markers.
 */
export function mergeCLAUDEmd(existing: string, incoming: string): string {
  if (!existing.trim()) {
    return `${START_MARKER}\n${incoming}\n${END_MARKER}\n`;
  }

  if (existing.includes(START_MARKER)) {
    const start = existing.indexOf(START_MARKER);
    const end = existing.indexOf(END_MARKER);
    if (end !== -1) {
      return (
        existing.slice(0, start) +
        `${START_MARKER}\n${incoming}\n${END_MARKER}` +
        existing.slice(end + END_MARKER.length)
      );
    }
  }

  return `${existing}\n\n---\n\n${START_MARKER}\n${incoming}\n${END_MARKER}\n`;
}
