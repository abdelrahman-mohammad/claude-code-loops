import { describe, it, expect } from "vitest";
import { mergeCLAUDEmd } from "../../src/utils/merge-claude-md.js";

const START_MARKER = "<!-- Generated by claude-code-loops -->";
const END_MARKER = "<!-- End claude-code-loops section -->";

describe("mergeCLAUDEmd", () => {
  it("when existing is empty, wraps incoming with markers", () => {
    const result = mergeCLAUDEmd("", "New content here");
    expect(result).toBe(
      `${START_MARKER}\nNew content here\n${END_MARKER}\n`,
    );
  });

  it("when no markers exist, appends with separator and markers", () => {
    const existing = "# My Project\n\nExisting documentation.";
    const result = mergeCLAUDEmd(existing, "Generated rules");
    expect(result).toBe(
      `${existing}\n\n---\n\n${START_MARKER}\nGenerated rules\n${END_MARKER}\n`,
    );
  });

  it("when markers already exist, replaces the section between them", () => {
    const existing = [
      "# My Project",
      "",
      START_MARKER,
      "Old generated content",
      END_MARKER,
      "",
      "Footer content",
    ].join("\n");

    const result = mergeCLAUDEmd(existing, "Updated content");
    expect(result).toContain(`${START_MARKER}\nUpdated content\n${END_MARKER}`);
    expect(result).not.toContain("Old generated content");
  });

  it("preserves content before and after the generated section", () => {
    const existing = [
      "# Header",
      "",
      START_MARKER,
      "Old stuff",
      END_MARKER,
      "",
      "# Footer",
    ].join("\n");

    const result = mergeCLAUDEmd(existing, "New stuff");
    expect(result).toContain("# Header");
    expect(result).toContain("# Footer");
    expect(result).toContain(`${START_MARKER}\nNew stuff\n${END_MARKER}`);
  });

  it("handles incoming content with special characters", () => {
    const incoming = 'Use $HOME and `backticks` and "quotes" & <tags>';
    const result = mergeCLAUDEmd("", incoming);
    expect(result).toContain(incoming);
    expect(result).toBe(`${START_MARKER}\n${incoming}\n${END_MARKER}\n`);
  });
});
